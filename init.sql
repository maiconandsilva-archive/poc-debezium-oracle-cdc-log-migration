-- Script idempotente para inicializar Oracle + Debezium de forma deterministica.

-- 1) Objetos de aplicacao no PDB
ALTER SESSION SET CONTAINER = FREEPDB1;

DECLARE
    l_exists NUMBER := 0;
BEGIN
    SELECT COUNT(*)
      INTO l_exists
      FROM ALL_TABLES
     WHERE OWNER = 'TEST'
       AND TABLE_NAME = 'CUSTOMERS';

    IF l_exists = 0 THEN
        EXECUTE IMMEDIATE q'[
            CREATE TABLE TEST.CUSTOMERS (
                id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                first_name VARCHAR2(255),
                last_name VARCHAR2(255),
                email VARCHAR2(255)
            )
        ]';
    END IF;
END;
/

BEGIN
    EXECUTE IMMEDIATE 'ALTER TABLE TEST.CUSTOMERS ADD SUPPLEMENTAL LOG DATA (ALL) COLUMNS';
EXCEPTION
    WHEN OTHERS THEN
        -- ORA-32588: supplemental log ja configurado para a tabela.
        IF SQLCODE != -32588 THEN
            RAISE;
        END IF;
END;
/

-- 2) Configuracoes globais no CDB$ROOT
ALTER SESSION SET CONTAINER = CDB$ROOT;

DECLARE
    l_supp VARCHAR2(3);
BEGIN
    SELECT SUPPLEMENTAL_LOG_DATA_MIN
      INTO l_supp
      FROM V$DATABASE;

    IF l_supp != 'YES' THEN
        EXECUTE IMMEDIATE 'ALTER DATABASE ADD SUPPLEMENTAL LOG DATA';
    END IF;
END;
/

DECLARE
    l_exists NUMBER := 0;
BEGIN
    SELECT COUNT(*)
      INTO l_exists
      FROM CDB_USERS
     WHERE USERNAME = 'C##DBZUSER';

    IF l_exists = 0 THEN
        EXECUTE IMMEDIATE 'CREATE USER c##dbzuser IDENTIFIED BY dbz CONTAINER=ALL';
    END IF;
END;
/

-- Garante senha esperada ao recriar ambiente.
ALTER USER c##dbzuser IDENTIFIED BY dbz;

-- Privilegios comuns necessarios para Debezium + LogMiner.
GRANT CREATE SESSION TO c##dbzuser CONTAINER=ALL;
GRANT SET CONTAINER TO c##dbzuser CONTAINER=ALL;
GRANT LOGMINING TO c##dbzuser CONTAINER=ALL;
GRANT SELECT_CATALOG_ROLE TO c##dbzuser CONTAINER=ALL;
GRANT SELECT ANY TRANSACTION TO c##dbzuser CONTAINER=ALL;
GRANT EXECUTE ON DBMS_LOGMNR TO c##dbzuser CONTAINER=ALL;
GRANT EXECUTE ON DBMS_LOGMNR_D TO c##dbzuser CONTAINER=ALL;
GRANT SELECT ON V_$LOGMNR_CONTENTS TO c##dbzuser CONTAINER=ALL;
GRANT SELECT ON V_$LOGMNR_LOGS TO c##dbzuser CONTAINER=ALL;
GRANT SELECT ON V_$LOGMNR_DICTIONARY TO c##dbzuser CONTAINER=ALL;
GRANT SELECT ON V_$LOGMNR_PARAMETERS TO c##dbzuser CONTAINER=ALL;
GRANT SELECT ON V_$DATABASE TO c##dbzuser CONTAINER=ALL;
GRANT SELECT ON V_$LOG TO c##dbzuser CONTAINER=ALL;
GRANT SELECT ON V_$LOGFILE TO c##dbzuser CONTAINER=ALL;
GRANT SELECT ON V_$ARCHIVED_LOG TO c##dbzuser CONTAINER=ALL;
GRANT SELECT ON V_$ARCHIVE_DEST_STATUS TO c##dbzuser CONTAINER=ALL;
GRANT SELECT ON V_$TRANSACTION TO c##dbzuser CONTAINER=ALL;
GRANT SELECT ON V_$MYSTAT TO c##dbzuser CONTAINER=ALL;
GRANT SELECT ON V_$STATNAME TO c##dbzuser CONTAINER=ALL;

-- 3) Privilegios no PDB para snapshot/streaming e flush table do conector.
ALTER SESSION SET CONTAINER = FREEPDB1;

GRANT CREATE TABLE TO c##dbzuser;
GRANT SELECT ANY TABLE TO c##dbzuser;
GRANT FLASHBACK ANY TABLE TO c##dbzuser;
GRANT SELECT ON TEST.CUSTOMERS TO c##dbzuser;
ALTER USER c##dbzuser QUOTA UNLIMITED ON USERS;
